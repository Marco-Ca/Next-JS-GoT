import { useState, useEffect } from 'react'
import Head from 'next/head'
import Link from "next/link";
import Image from 'next/image'
import styles from '../styles/Home.module.css'

const superHeroApi = `https://thronesapi.com/api/v2/Characters`;

export async function getServerSideProps() {
  const res = await fetch(superHeroApi);
  const data = await res.json();
  return {
    props: { data }
  }
}

export default function Home({ data }) {
  const [page, setPage] = useState({ start: 0, end: 4 })
  let characters = data.slice(page.start, page.end);

  const [state, setState] = useState(characters)
  const onLoadMore = () => {
    setPage({ start: page.start + 4, end: page.end + 4 })
    setState([...state, ...data.slice(page.start, page.end)])
  }

  useEffect(() => {
    setPage({ start: page.start + 4, end: page.end + 4 })
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  return (
    <div className={styles.container}>
      <Head>
        <title>Game of Thrones Characters</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <h1 className={styles.title}>
          Game of Thrones Characters
        </h1>
        <p className={styles.description}>
          <em>Winter is Coming</em>
        </p>
        <ul className={styles.grid}>
          {state.map(result => {
            const { id, fullName, imageUrl, title, family } = result;
            return (
              <li key={id} className={styles.card}>
                <a href="#">
                  <Image
                    placeholder="blur"
                    src={imageUrl}
                    alt={`${fullName}`}
                    width={200}
                    height={200}
                    blurDataURL={imageUrl}
                    loading="lazy"
                  />
                  <h5><em>{title}</em></h5>
                  <h3>{fullName}</h3>
                  <h6>{family}</h6>
                </a>
              </li>
            )
          })}
        </ul>
        <p>
          <h5 className={styles.loadmore} onClick={onLoadMore}>Load More</h5>
        </p>
      </main>
      <footer className={styles.footer}>
        &copy; Marco 2021
      </footer>
    </div>
  )
}
